const name1 = "Hanna";
const name2 = "Dmytro";

// 1) Concatenation
const greetingConcat = "Hello, " + name1 + " and " + name2 + "!";
console.log("Concatenation:", greetingConcat);

// 2) Template literal
const greetingTemplate = `Hello, ${name1} and ${name2}!`;
console.log("Template literal:", greetingTemplate);
